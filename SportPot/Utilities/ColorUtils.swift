import UIKit

extension UIColor {
    class var sp_mustard: UIColor {
        get {
            return UIColor(red: 0.98, green: 0.67, blue: 0.20, alpha: 1.00)
        }
    }

    class var sp_purple: UIColor {
        get {
            return UIColor(red: 0.65, green: 0.25, blue: 0.74, alpha: 1.00)
        }
    }

    class var sp_gray: UIColor {
        get {
            return UIColor(red: 0.91, green: 0.91, blue: 0.91, alpha: 1.00)
        }
    }

    class var sp_background: UIColor {
        get {
            return UIColor(red: 0.14, green: 0.18, blue: 0.20, alpha: 1.00)
        }
    }
    
    class var sp_light_background: UIColor {
        get {
            return UIColor(red: 35/255, green: 47/255, blue: 52/255, alpha: 1.00)
        }
    }
    
    class var sp_navigationBar: UIColor {
        get {
            return UIColor(red: 0.11, green: 0.15, blue: 0.16, alpha: 1.00)
        }
    }
}
